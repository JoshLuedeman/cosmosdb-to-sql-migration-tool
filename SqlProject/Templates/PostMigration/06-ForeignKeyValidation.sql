 06-ForeignKeyValidation.sql
 Generated by Cosmos-to-SQL Migration Assessment Tool

 Purpose
   Post-migration referential-integrity harness. Three checks per FK:
     1. Existence  -- every assessment-listed FK exists in sys.foreign_keys.
     2. Trust      -- is_not_trusted = 0 (post-bulk-import FKs are often
                      untrusted and the optimizer ignores them).
     3. Orphans    -- per-FK NULL-safe NOT EXISTS scan, including composites.

 Scope guard
   Only FKs whose child AND parent tables appear in the assessment's
   ContainerMappings are scanned. Out-of-scope FKs are summarised as a
   single INFO row so the report shows the gap without scanning unrelated
   500M-row tables.

 Throttle
   :setvar MaxOrphanScanRows "10000000"
   Child tables exceeding this row count (per sys.dm_db_partition_stats)
   are skipped with an INFO row -- the operator can lower or override.

 Required permissions
   - SELECT on every target schema/table.
   - VIEW DEFINITION on sys.foreign_keys (default for db_datareader+).
================================================================================
