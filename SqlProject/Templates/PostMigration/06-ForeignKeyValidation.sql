-- 2. Reusable orphan-check procedure (per FK).
-------------------------------------------------------------------------------
CREATE OR ALTER PROCEDURE dbo.sp_ValidationForeignKeyOrphans
    @RunId             UNIQUEIDENTIFIER,
    @FkObjectId        INT,
    @MaxOrphanScanRows BIGINT
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @FkName        SYSNAME,
            @ChildSchema   NVARCHAR(128),
            @ChildTable    NVARCHAR(128),
            @ParentSchema  NVARCHAR(128),
            @ParentTable   NVARCHAR(128),
            @ChildObjectId INT;

    SELECT  @FkName        = fk.name,
            @ChildSchema   = SCHEMA_NAME(ct.schema_id),
            @ChildTable    = ct.name,
            @ChildObjectId = ct.object_id,
            @ParentSchema  = SCHEMA_NAME(pt.schema_id),
            @ParentTable   = pt.name
    FROM    sys.foreign_keys AS fk
    JOIN    sys.tables       AS ct ON ct.object_id = fk.parent_object_id
    JOIN    sys.tables       AS pt ON pt.object_id = fk.referenced_object_id
    WHERE   fk.object_id = @FkObjectId;

    IF @FkName IS NULL
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, CheckName, Status, Details)
        VALUES (@RunId, N'ForeignKey', N'OrphanScan', N'FAIL',
                N'FK object_id ' + CAST(@FkObjectId AS NVARCHAR(20))
                + N' not found in sys.foreign_keys.');
        RETURN;
    END;

    DECLARE @ChildFullName  NVARCHAR(260) = QUOTENAME(@ChildSchema)  + N'.' + QUOTENAME(@ChildTable);
    DECLARE @ParentFullName NVARCHAR(260) = QUOTENAME(@ParentSchema) + N'.' + QUOTENAME(@ParentTable);

    -- Pre-flight row-count throttle.
    DECLARE @ChildRows BIGINT =
    (
        SELECT  SUM(row_count)
        FROM    sys.dm_db_partition_stats
        WHERE   object_id = @ChildObjectId
            AND index_id IN (0, 1)
    );

    IF @ChildRows IS NULL
        SET @ChildRows = 0;

    IF @ChildRows > @MaxOrphanScanRows
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, ObjectName, CheckName, Status, Details)
        VALUES (@RunId, N'ForeignKey', @ChildSchema, @ChildTable, @FkName, N'OrphanScan', N'INFO',
                N'Orphan scan skipped: child table row count ' + CAST(@ChildRows AS NVARCHAR(20))
                + N' exceeds MaxOrphanScanRows ' + CAST(@MaxOrphanScanRows AS NVARCHAR(20))
                + N'. Tune :setvar MaxOrphanScanRows to opt in.');
        RETURN;
    END;

    -- Build NULL-safe composite predicate from sys.foreign_key_columns.
    DECLARE @NullGuard NVARCHAR(MAX) = N'';
    DECLARE @JoinPred  NVARCHAR(MAX) = N'';

    SELECT  @NullGuard = STRING_AGG(N'c.' + QUOTENAME(cc.name) + N' IS NOT NULL', N' AND ')
                            WITHIN GROUP (ORDER BY fkc.constraint_column_id),
            @JoinPred  = STRING_AGG(N'p.' + QUOTENAME(pc.name) + N' = c.' + QUOTENAME(cc.name), N' AND ')
                            WITHIN GROUP (ORDER BY fkc.constraint_column_id)
    FROM    sys.foreign_key_columns AS fkc
    JOIN    sys.columns AS cc ON cc.object_id = fkc.parent_object_id AND cc.column_id = fkc.parent_column_id
    JOIN    sys.columns AS pc ON pc.object_id = fkc.referenced_object_id AND pc.column_id = fkc.referenced_column_id
    WHERE   fkc.constraint_object_id = @FkObjectId;

    IF @NullGuard IS NULL OR @JoinPred IS NULL
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, ObjectName, CheckName, Status, Details)
        VALUES (@RunId, N'ForeignKey', @ChildSchema, @ChildTable, @FkName, N'OrphanScan', N'FAIL',
                N'No columns found in sys.foreign_key_columns for FK ' + @FkName + N'.');
        RETURN;
    END;

    DECLARE @Sql NVARCHAR(MAX) = N'
        SELECT @Orphans = COUNT_BIG(*)
        FROM   ' + @ChildFullName + N' AS c WITH (NOLOCK)
        WHERE  ' + @NullGuard + N'
          AND NOT EXISTS (
                SELECT 1
                FROM   ' + @ParentFullName + N' AS p WITH (NOLOCK)
                WHERE  ' + @JoinPred + N'
          );';

    DECLARE @Orphans BIGINT = 0;
    BEGIN TRY
        EXEC sys.sp_executesql @Sql, N'@Orphans BIGINT OUTPUT', @Orphans = @Orphans OUTPUT;
    END TRY
    BEGIN CATCH
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, ObjectName, CheckName, Status, Details)
        VALUES (@RunId, N'ForeignKey', @ChildSchema, @ChildTable, @FkName, N'OrphanScan', N'FAIL',
                N'Error running orphan scan for ' + @FkName + N': ' + ERROR_MESSAGE());
        RETURN;
    END CATCH;

    DECLARE @Status  NVARCHAR(16);
    DECLARE @Details NVARCHAR(MAX);
    IF @Orphans = 0
    BEGIN
        SET @Status  = N'PASS';
        SET @Details = N'No orphan rows in ' + @ChildFullName + N' (scanned '
                     + CAST(@ChildRows AS NVARCHAR(20)) + N' row(s)).';
    END
    ELSE
    BEGIN
        SET @Status  = N'FAIL';
        SET @Details = CAST(@Orphans AS NVARCHAR(20)) + N' orphan row(s) detected in '
                     + @ChildFullName + N' referencing missing parent in '
                     + @ParentFullName + N'.';
    END;

    INSERT dbo.ValidationResults
        (RunId, Category, SchemaName, TableName, ObjectName, CheckName,
         ExpectedValue, ActualValue, Status, Details)
    VALUES
        (@RunId, N'ForeignKey', @ChildSchema, @ChildTable, @FkName, N'OrphanScan',
         N'0', CAST(@Orphans AS NVARCHAR(20)), @Status, @Details);
END;
GO

