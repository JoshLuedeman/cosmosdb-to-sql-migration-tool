-- 3. Main validation batch.
-------------------------------------------------------------------------------
BEGIN
    DECLARE @RunId UNIQUEIDENTIFIER = TRY_CAST(N'$(RunId)' AS UNIQUEIDENTIFIER);
    IF @RunId IS NULL OR @RunId = CAST(N'00000000-0000-0000-0000-000000000000' AS UNIQUEIDENTIFIER)
    BEGIN
        SET @RunId = NEWID();
        PRINT N'WARNING: No orchestrator RunId supplied; generated local RunId = '
              + CONVERT(NVARCHAR(36), @RunId)
              + N'. Standalone run -- results will not appear in the cross-script report.';
    END;

    DECLARE @MaxOrphanScanRows BIGINT = ISNULL(TRY_CAST(N'$(MaxOrphanScanRows)' AS BIGINT), 10000000);

    -- {{ExpectedForeignKeysSeed}}
{{ExpectedForeignKeysSeed}}

    -- {{FkScopeTablesSeed}}
{{FkScopeTablesSeed}}

    -------------------------------------------------------------------------
    -- 3a. Existence + column-shape + trust + cascade-action checks.
    -------------------------------------------------------------------------
    ;WITH discovered AS (
        SELECT  fk.object_id                  AS FkObjectId,
                fk.name                       AS FkName,
                SCHEMA_NAME(ct.schema_id)     AS ChildSchema,
                ct.name                       AS ChildTable,
                cc.name                       AS ChildColumn,
                SCHEMA_NAME(pt.schema_id)     AS ParentSchema,
                pt.name                       AS ParentTable,
                pc.name                       AS ParentColumn,
                fk.is_not_trusted             AS IsNotTrusted,
                fk.delete_referential_action_desc AS OnDeleteAction,
                fk.update_referential_action_desc AS OnUpdateAction,
                ROW_NUMBER() OVER (PARTITION BY fk.object_id
                                   ORDER BY fkc.constraint_column_id) AS ColPosition,
                COUNT(*) OVER (PARTITION BY fk.object_id)              AS ColCount
        FROM    sys.foreign_keys        AS fk
        JOIN    sys.foreign_key_columns AS fkc ON fkc.constraint_object_id = fk.object_id
        JOIN    sys.tables              AS ct  ON ct.object_id  = fk.parent_object_id
        JOIN    sys.tables              AS pt  ON pt.object_id  = fk.referenced_object_id
        JOIN    sys.columns             AS cc  ON cc.object_id  = fkc.parent_object_id
                                              AND cc.column_id  = fkc.parent_column_id
        JOIN    sys.columns             AS pc  ON pc.object_id  = fkc.referenced_object_id
                                              AND pc.column_id  = fkc.referenced_column_id
    ),
    -- Map cascade-action enum to short tokens for comparison with the assessment.
    discovered_norm AS (
        SELECT  d.*,
                REPLACE(d.OnDeleteAction, N'_', N' ') AS OnDeleteActionNorm,
                REPLACE(d.OnUpdateAction, N'_', N' ') AS OnUpdateActionNorm
        FROM    discovered AS d
    )
    INSERT dbo.ValidationResults
        (RunId, Category, SchemaName, TableName, ObjectName, CheckName,
         ExpectedValue, ActualValue, Status, Details)
    SELECT  @RunId, N'ForeignKey',
            e.ChildSchema, e.ChildTable, e.FkName, N'Definition',
            e.ChildSchema + N'.' + e.ChildTable + N'.' + e.ChildColumn
            + N' -> ' + e.ParentSchema + N'.' + e.ParentTable + N'.' + e.ParentColumn,
            COALESCE(d.ChildSchema + N'.' + d.ChildTable + N'.' + d.ChildColumn
                     + N' -> ' + d.ParentSchema + N'.' + d.ParentTable + N'.' + d.ParentColumn,
                     N'<missing>'),
            CASE
                WHEN d.FkObjectId IS NULL                          THEN N'FAIL'
                WHEN d.ColCount   <> 1                             THEN N'WARN'  -- composite: assessment only models single-col
                WHEN d.ChildSchema  <> e.ChildSchema
                  OR d.ChildTable   <> e.ChildTable
                  OR d.ChildColumn  <> e.ChildColumn
                  OR d.ParentSchema <> e.ParentSchema
                  OR d.ParentTable  <> e.ParentTable
                  OR d.ParentColumn <> e.ParentColumn              THEN N'FAIL'
                WHEN d.IsNotTrusted = 1                            THEN N'WARN'
                WHEN d.OnDeleteActionNorm <> e.OnDeleteAction
                  OR d.OnUpdateActionNorm <> e.OnUpdateAction      THEN N'WARN'
                ELSE                                                    N'PASS'
            END,
            CASE
                WHEN d.FkObjectId IS NULL THEN
                    N'FK [' + e.FkName + N'] not found in sys.foreign_keys.'
                WHEN d.ColCount <> 1 THEN
                    N'FK is composite (' + CAST(d.ColCount AS NVARCHAR(10))
                    + N' cols); assessment only models single-column FKs. Manual verification recommended.'
                WHEN d.ChildSchema  <> e.ChildSchema
                  OR d.ChildTable   <> e.ChildTable
                  OR d.ChildColumn  <> e.ChildColumn
                  OR d.ParentSchema <> e.ParentSchema
                  OR d.ParentTable  <> e.ParentTable
                  OR d.ParentColumn <> e.ParentColumn THEN
                    N'FK shape drift: expected '
                    + e.ChildSchema + N'.' + e.ChildTable + N'.' + e.ChildColumn
                    + N' -> ' + e.ParentSchema + N'.' + e.ParentTable + N'.' + e.ParentColumn
                    + N'; actual ' + d.ChildSchema + N'.' + d.ChildTable + N'.' + d.ChildColumn
                    + N' -> ' + d.ParentSchema + N'.' + d.ParentTable + N'.' + d.ParentColumn
                WHEN d.IsNotTrusted = 1 THEN
                    N'FK is not trusted. Remediation: ALTER TABLE '
                    + QUOTENAME(d.ChildSchema) + N'.' + QUOTENAME(d.ChildTable)
                    + N' WITH CHECK CHECK CONSTRAINT ' + QUOTENAME(d.FkName) + N';'
                WHEN d.OnDeleteActionNorm <> e.OnDeleteAction
                  OR d.OnUpdateActionNorm <> e.OnUpdateAction THEN
                    N'Cascade-action mismatch: expected ON DELETE ' + e.OnDeleteAction
                    + N' / ON UPDATE ' + e.OnUpdateAction
                    + N'; actual ON DELETE ' + d.OnDeleteActionNorm
                    + N' / ON UPDATE ' + d.OnUpdateActionNorm
                ELSE
                    N'FK definition matches assessment, is trusted, and cascade actions agree.'
            END
    FROM    dbo.ValidationExpectedForeignKeys AS e
    LEFT    JOIN discovered_norm AS d
            ON  d.FkName = e.FkName
            AND d.ColPosition = 1;     -- single representative row per FK

    -------------------------------------------------------------------------
    -- 3b. Per-FK orphan scan (scope-limited).
    -------------------------------------------------------------------------
    DECLARE @InScopeFks TABLE (FkObjectId INT PRIMARY KEY);
    INSERT @InScopeFks (FkObjectId)
    SELECT  fk.object_id
    FROM    sys.foreign_keys AS fk
    JOIN    sys.tables       AS ct ON ct.object_id = fk.parent_object_id
    JOIN    sys.tables       AS pt ON pt.object_id = fk.referenced_object_id
    JOIN    dbo.ValidationFkScopeTables AS sc
            ON sc.SchemaName = SCHEMA_NAME(ct.schema_id) AND sc.TableName = ct.name
    JOIN    dbo.ValidationFkScopeTables AS sp
            ON sp.SchemaName = SCHEMA_NAME(pt.schema_id) AND sp.TableName = pt.name;

    -- Out-of-scope FK summary.
    DECLARE @OutOfScopeNames NVARCHAR(MAX) = NULL;
    SELECT  @OutOfScopeNames = STRING_AGG(fk.name, N', ') WITHIN GROUP (ORDER BY fk.name)
    FROM    sys.foreign_keys AS fk
    WHERE   fk.object_id NOT IN (SELECT FkObjectId FROM @InScopeFks);

    IF @OutOfScopeNames IS NOT NULL
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, CheckName, Status, Details)
        VALUES (@RunId, N'ForeignKey', N'OutOfScopeFks', N'INFO',
                N'FKs in the database but outside the assessment scope (not scanned): '
                + @OutOfScopeNames);
    END;

    DECLARE @FkObjectId INT;
    DECLARE fk_cursor CURSOR LOCAL FAST_FORWARD FOR
        SELECT FkObjectId FROM @InScopeFks;

    OPEN fk_cursor;
    FETCH NEXT FROM fk_cursor INTO @FkObjectId;
    WHILE @@FETCH_STATUS = 0
    BEGIN
        BEGIN TRY
            EXEC dbo.sp_ValidationForeignKeyOrphans
                @RunId             = @RunId,
                @FkObjectId        = @FkObjectId,
                @MaxOrphanScanRows = @MaxOrphanScanRows;
        END TRY
        BEGIN CATCH
            INSERT dbo.ValidationResults (RunId, Category, CheckName, Status, Details)
            VALUES (@RunId, N'ForeignKey', N'OrphanScan', N'FAIL',
                    N'Unhandled error scanning FK object_id ' + CAST(@FkObjectId AS NVARCHAR(20))
                    + N': ' + ERROR_MESSAGE());
        END CATCH;
        FETCH NEXT FROM fk_cursor INTO @FkObjectId;
    END;
    CLOSE fk_cursor;
    DEALLOCATE fk_cursor;

    DECLARE @Pass INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'ForeignKey' AND Status = N'PASS');
    DECLARE @Warn INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'ForeignKey' AND Status = N'WARN');
    DECLARE @Fail INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'ForeignKey' AND Status = N'FAIL');
    DECLARE @Info INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'ForeignKey' AND Status = N'INFO');
    PRINT N'[ForeignKey] RunId=' + CONVERT(NVARCHAR(36), @RunId)
        + N' Pass=' + CAST(@Pass AS NVARCHAR(20))
        + N' Warn=' + CAST(@Warn AS NVARCHAR(20))
        + N' Fail=' + CAST(@Fail AS NVARCHAR(20))
        + N' Info=' + CAST(@Info AS NVARCHAR(20));
END;
