/*
================================================================================
 04-PerformanceBaseline.sql
 Generated by Cosmos-to-SQL Migration Assessment Tool

 Purpose
   Post-migration performance baseline. Observational only -- captures the
   *current* state of database health into dbo.ValidationResults so the
   operator has a baseline to compare against on subsequent runs.

 Sections
   01. TableSize          -- per-mapped-table row & page counts (sys.dm_db_partition_stats)
   02. IndexCount         -- per-table NC index count (sys.indexes)
   03. PlanCacheWarmth    -- gating INFO row about plan cache fullness
   04. WaitStats          -- top 20 waits from sys.dm_db_wait_stats (excl. benign)
   05. TopCpu             -- top 20 plan-cache entries by total CPU, scoped to mapped tables
   06. TopDuration        -- top 20 plan-cache entries by total elapsed, scoped to mapped tables
   07. MissingIndex       -- missing-index suggestions, scoped to mapped tables
   08. MissingIndexDmvAge -- INFO row with sqlserver_start_time for context
   09. StatsFreshness     -- STATS_DATE per stat on mapped tables
   10. Summary

 Permissions
   - SELECT on every mapped schema/table.
   - VIEW DATABASE STATE for DMV access (sections 03-09).
     Pre-flight check emits a WARN + skip if absent.

 Notes
   - All rows are INFO by default. The following thresholds escalate to WARN:
       * Stats older than 30 days on a table with > 100k rows
       * STATS_DATE IS NULL on a table with > 100k rows
       * Missing-index improvement_measure > :setvar MissingIndexWarnThreshold
       * Any wait type in (PAGEIOLATCH_*) > 10% of total signal wait time

   - Plan-cache filtering uses LIKE on sys.dm_exec_sql_text -- this is
     approximate (a string literal containing a table name will false-positive).
     Acceptable for a baseline; use Query Store for precise attribution.

   - The plan cache is bounded and resets on Azure SQL DB maintenance restarts.
     Run this after at least 1 hour of representative workload.

   - Fragmentation (sys.dm_db_index_physical_stats) is deferred to a separate
     index-health script -- LIMITED mode still scans index pages and can be
     expensive on large tables.

 Re-use
   Re-uses dbo.ValidationFkScopeTables (created by 06-ForeignKeyValidation.sql)
   to scope to assessment-mapped tables only. Re-creates it if missing.
================================================================================
*/

