-- 1. Seed scope table (idempotent).
-------------------------------------------------------------------------------
IF OBJECT_ID(N'dbo.ValidationFkScopeTables', N'U') IS NULL
BEGIN
    CREATE TABLE dbo.ValidationFkScopeTables
    (
        SchemaName NVARCHAR(128) NOT NULL,
        TableName  NVARCHAR(128) NOT NULL,
        CONSTRAINT PK_ValidationFkScopeTables PRIMARY KEY (SchemaName, TableName)
    );
END;
