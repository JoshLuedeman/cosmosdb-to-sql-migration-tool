/*
================================================================================
 05-IndexValidation.sql
 Generated by Cosmos-to-SQL Migration Assessment Tool

 Purpose
   Post-migration index validation. For every index recommended by the
   assessment:
     1. Existence    -- index exists in sys.indexes (matched by schema/table/name).
     2. Enabled      -- is_disabled = 0 (emit ALTER INDEX ... REBUILD hint otherwise).
     3. Type         -- type_desc matches (with COLUMNSTORE tolerance).
     4. Uniqueness   -- is_unique matches (skipped for COLUMNSTORE actual).
     5. Key columns  -- ordered, case-insensitive comma-joined match.
     6. Included     -- ordered, case-insensitive match (drift = WARN, not FAIL).
     7. Filter       -- actual has a filter but expected does not (WARN).

   Plus a discovery sweep that surfaces "unexpected" physical indexes -- any
   index in sys.indexes on a mapped table that is NOT in the assessment.
   PRIMARY KEYs, HEAPs, and hypothetical indexes are filtered to avoid noise.

 Output
   One row per expected index in dbo.ValidationResults
   (Category = N'Index', CheckName = N'Definition').
   Optional INFO rows for unexpected indexes (CheckName = N'Unexpected').

 Required permissions
   - VIEW DEFINITION on sys.indexes (default for db_datareader+).

 Re-use
   Re-uses dbo.ValidationFkScopeTables (created by 06-ForeignKeyValidation.sql)
   to scope the "unexpected index" sweep to assessment-mapped tables only.
   Run 06 first, or this script will create the scope table if needed.

 NOTE on COLUMNSTORE
   Assessment's IndexType = "ColumnStore" is rendered here as expected
   type NONCLUSTERED COLUMNSTORE. The comparison treats CLUSTERED COLUMNSTORE
   actual as also matching expected ColumnStore (different code paths in
   SqlProjectGenerationService vs SqlDatabaseProjectService may emit either).
================================================================================
*/

