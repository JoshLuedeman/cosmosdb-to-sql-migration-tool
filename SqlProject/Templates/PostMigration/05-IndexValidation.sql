    (
        SchemaName       NVARCHAR(128) NOT NULL,
        TableName        NVARCHAR(128) NOT NULL,
        IndexName        NVARCHAR(128) NOT NULL,
        ExpectedType     NVARCHAR(64)  NOT NULL,
        IsUnique         BIT           NOT NULL,
        KeyColumns       NVARCHAR(MAX) NOT NULL,
        IncludedColumns  NVARCHAR(MAX) NOT NULL,
        CapturedAt       DATETIME2(3)  NOT NULL
            CONSTRAINT DF_VExpIdx_CapturedAt DEFAULT SYSUTCDATETIME(),
        CONSTRAINT PK_ValidationExpectedIndexes
