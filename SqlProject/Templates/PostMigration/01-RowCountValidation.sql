-- 4. Main validation batch: resolve RunId, seed expected counts, compare.
-------------------------------------------------------------------------------
BEGIN
    DECLARE @RunId UNIQUEIDENTIFIER = TRY_CAST(N'$(RunId)' AS UNIQUEIDENTIFIER);
    IF @RunId IS NULL OR @RunId = CAST(N'00000000-0000-0000-0000-000000000000' AS UNIQUEIDENTIFIER)
    BEGIN
        SET @RunId = NEWID();
        PRINT N'WARNING: No orchestrator RunId supplied; generated local RunId = '
              + CONVERT(NVARCHAR(36), @RunId)
              + N'. Standalone run -- results will not appear in the cross-script report.';
    END;

    DECLARE @TolerancePctPass DECIMAL(9,4) = ISNULL(TRY_CAST(N'$(TolerancePctPass)' AS DECIMAL(9,4)), 0.5);
    DECLARE @TolerancePctWarn DECIMAL(9,4) = ISNULL(TRY_CAST(N'$(TolerancePctWarn)' AS DECIMAL(9,4)), 5.0);

    -- Permission pre-flight.
    DECLARE @CanUseDmv BIT = ISNULL(HAS_PERMS_BY_NAME(NULL, NULL, N'VIEW DATABASE STATE'), 0);
    IF @CanUseDmv = 0
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, CheckName, Status, Details)
        VALUES (@RunId, N'RowCount', N'PreflightPermissionCheck', N'WARN',
                N'Caller lacks VIEW DATABASE STATE; falling back to COUNT_BIG(*) for every table.');
    END;

    -- Seed expected counts from the assessment. Idempotent: assessment rows are
    -- replaced on every run. Override rows (Source='Override') are owned by the
    -- orchestrator (#198) and left alone here.
    -- {{ExpectedCountsSeed}} -- generator inserts MERGE statements here
{{ExpectedCountsSeed}}

    -- Per-table comparison. The generator emits one block per migrated table.
    -- Each block calls dbo.sp_ValidationRowCountCheck (created just below).
    -- {{TableRowsBlock}} -- generator inserts EXEC calls here
{{TableRowsBlock}}

    -- Summary.
    DECLARE @Pass INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'RowCount' AND Status = N'PASS');
    DECLARE @Warn INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'RowCount' AND Status = N'WARN');
    DECLARE @Fail INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'RowCount' AND Status = N'FAIL');
    DECLARE @Info INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'RowCount' AND Status = N'INFO');
    PRINT N'[RowCount] RunId=' + CONVERT(NVARCHAR(36), @RunId)
        + N' Pass=' + CAST(@Pass AS NVARCHAR(20))
        + N' Warn=' + CAST(@Warn AS NVARCHAR(20))
        + N' Fail=' + CAST(@Fail AS NVARCHAR(20))
        + N' Info=' + CAST(@Info AS NVARCHAR(20));
END;
