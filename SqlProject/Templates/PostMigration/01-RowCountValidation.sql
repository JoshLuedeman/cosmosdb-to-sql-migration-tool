-- 3. Reusable per-table comparison procedure.
--    Encapsulates the dual-strategy count (fast DMV first, exact COUNT_BIG(*)
--    on mismatch or when the caller lacks VIEW DATABASE STATE).
-------------------------------------------------------------------------------
CREATE OR ALTER PROCEDURE dbo.sp_ValidationRowCountCheck
    @RunId             UNIQUEIDENTIFIER,
    @SchemaName        SYSNAME,
    @TableName         SYSNAME,
    @CanUseDmv         BIT,
    @TolerancePctPass  DECIMAL(9,4),
    @TolerancePctWarn  DECIMAL(9,4)
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @ObjectId     INT          = OBJECT_ID(QUOTENAME(@SchemaName) + N'.' + QUOTENAME(@TableName));
    DECLARE @FullName     NVARCHAR(260) = QUOTENAME(@SchemaName) + N'.' + QUOTENAME(@TableName);
    DECLARE @Expected     BIGINT;
    DECLARE @Actual       BIGINT;
    DECLARE @Source       NVARCHAR(32);
    DECLARE @Status       NVARCHAR(16);
    DECLARE @Details      NVARCHAR(MAX);

    IF @ObjectId IS NULL
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'RowCount', @SchemaName, @TableName, N'TableExists', N'FAIL',
                N'Target table ' + @FullName + N' does not exist.');
        RETURN;
    END;

    -- Pick the expected value: override beats assessment.
    SELECT TOP (1) @Expected = ExpectedRows, @Source = Source
    FROM   dbo.ValidationExpectedCounts
    WHERE  SchemaName = @SchemaName AND TableName = @TableName
    ORDER BY CASE Source WHEN N'Override' THEN 0 WHEN N'Assessment' THEN 1 ELSE 2 END;

    -- Fast metadata path.
    IF @CanUseDmv = 1
    BEGIN
        SELECT @Actual = SUM(row_count)
        FROM   sys.dm_db_partition_stats
        WHERE  object_id = @ObjectId
          AND  index_id IN (0, 1);
    END;

    -- Exact fallback when DMV unavailable, missing, or mismatched.
    IF @CanUseDmv = 0
        OR @Actual IS NULL
        OR (@Expected IS NOT NULL AND @Actual <> @Expected)
    BEGIN
        DECLARE @Sql NVARCHAR(MAX) =
            N'SELECT @cnt = COUNT_BIG(*) FROM ' + @FullName + N' WITH (NOLOCK);';
        EXEC sys.sp_executesql @Sql, N'@cnt BIGINT OUTPUT', @cnt = @Actual OUTPUT;
    END;

    IF @Expected IS NULL
    BEGIN
        SET @Status  = N'WARN';
        SET @Details = N'No expected baseline (assessment or override) for ' + @FullName + N'.';
    END
    ELSE IF @Expected = 0 AND @Actual > 0
    BEGIN
        SET @Status  = N'WARN';
        SET @Details = N'Assessment baseline was 0; actual is ' + CAST(@Actual AS NVARCHAR(20))
                     + N'. Container likely populated after assessment.';
    END
    ELSE
    BEGIN
        DECLARE @DeltaPct DECIMAL(18,6) =
            CASE
                WHEN @Expected = 0 THEN CASE WHEN @Actual = 0 THEN 0 ELSE 100 END
                ELSE ABS(CAST(@Actual - @Expected AS DECIMAL(38,6)) * 100.0
                         / NULLIF(CAST(@Expected AS DECIMAL(38,6)), 0))
            END;

        SET @Status =
            CASE
                WHEN @DeltaPct <= @TolerancePctPass THEN N'PASS'
                WHEN @DeltaPct <= @TolerancePctWarn THEN N'WARN'
                ELSE N'FAIL'
            END;

        SET @Details = N'Delta=' + CAST(@DeltaPct AS NVARCHAR(40)) + N'% (tolerance pass<='
                     + CAST(@TolerancePctPass AS NVARCHAR(20)) + N'%, warn<='
                     + CAST(@TolerancePctWarn AS NVARCHAR(20)) + N'%) source='
                     + ISNULL(@Source, N'(none)');
    END;

    INSERT dbo.ValidationResults
        (RunId, Category, SchemaName, TableName, CheckName, ExpectedValue, ActualValue, Status, Details)
    VALUES
        (@RunId, N'RowCount', @SchemaName, @TableName, N'RowCount',
         CASE WHEN @Expected IS NULL THEN NULL ELSE CAST(@Expected AS NVARCHAR(50)) END,
         CASE WHEN @Actual   IS NULL THEN NULL ELSE CAST(@Actual   AS NVARCHAR(50)) END,
         @Status, @Details);
END;
GO

