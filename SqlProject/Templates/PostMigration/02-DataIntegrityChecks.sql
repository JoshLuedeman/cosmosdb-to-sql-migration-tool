-- 2. Reusable per-table checksum procedure.
-------------------------------------------------------------------------------
CREATE OR ALTER PROCEDURE dbo.sp_ValidationChecksum
    @RunId        UNIQUEIDENTIFIER,
    @SchemaName   SYSNAME,
    @TableName    SYSNAME,
    @SampleRows   BIGINT,
    @ColumnList   NVARCHAR(MAX),    -- pre-rendered CONCAT_WS argument list
    @OrderBy      NVARCHAR(MAX)     -- ORDER BY clause (no leading 'ORDER BY')
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @ObjectId   INT          = OBJECT_ID(QUOTENAME(@SchemaName) + N'.' + QUOTENAME(@TableName));
    DECLARE @FullName   NVARCHAR(260) = QUOTENAME(@SchemaName) + N'.' + QUOTENAME(@TableName);

    IF @ObjectId IS NULL
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'Checksum', @SchemaName, @TableName, N'TableExists', N'FAIL',
                N'Target table ' + @FullName + N' does not exist.');
        RETURN;
    END;

    IF @ColumnList IS NULL OR LTRIM(RTRIM(@ColumnList)) = N''
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'Checksum', @SchemaName, @TableName, N'ColumnList', N'INFO',
                N'No column metadata available in the assessment; checksum skipped.');
        RETURN;
    END;

    DECLARE @TopClause NVARCHAR(64) =
        CASE WHEN @SampleRows > 0 THEN N'TOP (' + CAST(@SampleRows AS NVARCHAR(20)) + N') ' ELSE N'' END;

    DECLARE @Sql NVARCHAR(MAX) = N'
        DECLARE @AggInt BIGINT;
        DECLARE @Count  BIGINT;
        SELECT @AggInt = SUM(CAST(CHECKSUM(RowHash) AS BIGINT)),
               @Count  = COUNT_BIG(*)
        FROM (
            SELECT ' + @TopClause + N'HASHBYTES(N''SHA2_256'', CONCAT_WS(''||'', ' + @ColumnList + N')) AS RowHash
            FROM   ' + @FullName + N'
            ORDER  BY ' + @OrderBy + N'
        ) AS s;
        SELECT @AggInt AS AggInt, @Count AS RowCount;';

    DECLARE @Result TABLE (AggInt BIGINT, [RowCount] BIGINT);
    BEGIN TRY
        INSERT @Result (AggInt, [RowCount])
        EXEC sys.sp_executesql @Sql;
    END TRY
    BEGIN CATCH
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'Checksum', @SchemaName, @TableName, N'ExecuteHash', N'FAIL',
                N'Error computing checksum for ' + @FullName + N': ' + ERROR_MESSAGE());
        RETURN;
    END CATCH;

    DECLARE @Agg BIGINT, @Rows BIGINT;
    SELECT TOP (1) @Agg = AggInt, @Rows = [RowCount] FROM @Result;

    -- Hex-encoded fingerprint (16 hex chars; signed BIGINT representation).
    DECLARE @ActualHash NVARCHAR(64) = CONVERT(NVARCHAR(64), CAST(@Agg AS VARBINARY(8)), 2);

    DECLARE @Expected NVARCHAR(64);
    DECLARE @ExpectedSample BIGINT;
    SELECT TOP (1) @Expected = ExpectedHash, @ExpectedSample = SampleRows
    FROM   dbo.ValidationExpectedChecksums
    WHERE  SchemaName = @SchemaName AND TableName = @TableName;

    DECLARE @Status  NVARCHAR(16);
    DECLARE @Details NVARCHAR(MAX);

    IF @Expected IS NULL
    BEGIN
        SET @Status  = N'INFO';
        SET @Details = N'Fingerprint captured (' + CAST(@Rows AS NVARCHAR(20))
                     + N' rows hashed). No operator-supplied baseline.';
    END
    ELSE IF @ExpectedSample <> @SampleRows
    BEGIN
        SET @Status  = N'WARN';
        SET @Details = N'SampleRows mismatch: actual=' + CAST(@SampleRows AS NVARCHAR(20))
                     + N' expected=' + CAST(@ExpectedSample AS NVARCHAR(20));
    END
    ELSE IF @ActualHash = @Expected
    BEGIN
        SET @Status  = N'PASS';
        SET @Details = N'Hashes match (' + CAST(@Rows AS NVARCHAR(20)) + N' rows).';
    END
    ELSE
    BEGIN
        SET @Status  = N'FAIL';
        SET @Details = N'Hash mismatch (' + CAST(@Rows AS NVARCHAR(20)) + N' rows hashed).';
    END;

    INSERT dbo.ValidationResults
        (RunId, Category, SchemaName, TableName, CheckName, ExpectedValue, ActualValue, Status, Details)
    VALUES
        (@RunId, N'Checksum', @SchemaName, @TableName, N'Fingerprint',
         @Expected, @ActualHash, @Status, @Details);
END;
GO

