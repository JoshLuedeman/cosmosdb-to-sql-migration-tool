/*
================================================================================
 02-DataIntegrityChecks.sql
 Generated by Cosmos-to-SQL Migration Assessment Tool

 Purpose
   Produce a deterministic per-table data fingerprint (SHA2-256 per row, then
   CHECKSUM_AGG of the per-row hashes) and compare it to a baseline computed
   on the source Cosmos data.

 Contract for cross-platform reproducibility
   - Per-row hash: SHA2-256 over CONCAT_WS('||', col1, col2, ...).
   - Each column is cast to VARCHAR(MAX) so the hashed byte stream is UTF-8
     compatible (NVARCHAR would hash UTF-16LE bytes and never match a Python
     baseline).
   - NULLs are replaced by the literal string '\N' BEFORE concatenation so
     ('a', NULL, 'b') and ('a', 'b', NULL) produce distinct hashes.
   - Aggregate: CHECKSUM_AGG(CHECKSUM(rowhash)) -- commutative, no parallel
     plan ordering hazard. Trade-off: aggregate collision-resistance is
     CHECKSUM-level, not SHA2-level, but the per-row SHA2 already catches
     any single-row mutation.
   - To reproduce against Cosmos: order docs by the same key, project the
     same field set in the same order, convert each field to its UTF-8
     string form ('\N' for null), join with '||', SHA2-256 each row, then
     XOR all hash values interpreted as little-endian INT32 (first 4 bytes,
     matching SQL Server's CHECKSUM(varbinary) semantics).

 Status semantics
   ExpectedHash IS NULL         -> INFO  (fingerprint only)
   ActualHash  =  ExpectedHash  -> PASS
   otherwise                    -> FAIL

 Sampling
   :setvar SampleRows 10000     (0 = full table)
   Uses TOP (@SampleRows) with a deterministic ORDER BY so re-runs produce
   the same hash.

 Required permissions
   - SELECT on every target schema/table.
================================================================================
*/

