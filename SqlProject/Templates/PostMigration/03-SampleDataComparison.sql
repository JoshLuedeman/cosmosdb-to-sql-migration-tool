-- 3. Main validation batch.
-------------------------------------------------------------------------------
BEGIN
    DECLARE @RunId UNIQUEIDENTIFIER = TRY_CAST(N'$(RunId)' AS UNIQUEIDENTIFIER);
    IF @RunId IS NULL OR @RunId = CAST(N'00000000-0000-0000-0000-000000000000' AS UNIQUEIDENTIFIER)
    BEGIN
        SET @RunId = NEWID();
        PRINT N'WARNING: No orchestrator RunId supplied; generated local RunId = '
              + CONVERT(NVARCHAR(36), @RunId)
              + N'. Standalone run -- results will not appear in the cross-script report.';
    END;

    DECLARE @SampleRows INT = ISNULL(TRY_CAST(N'$(SampleRows)' AS INT), 10);
    IF @SampleRows <= 0 SET @SampleRows = 10;

    -- Per-table EXEC calls + child-table skip notices.
    -- {{SampleCaptureBlock}}
{{SampleCaptureBlock}}

    -------------------------------------------------------------------------
    -- 4. Diff snapshot vs operator-supplied baseline (FULL OUTER JOIN so
    --    rows missing on either side surface as INFO/FAIL respectively).
    -------------------------------------------------------------------------
    ;WITH actual_cur AS (
        SELECT SchemaName, TableName, Position, Ordinal, KeyHash, RowJson
        FROM   dbo.ValidationSampleRows
        WHERE  RunId = @RunId
    ),
    expected_cur AS (
        SELECT SchemaName, TableName, Position, Ordinal, KeyHash, RowJson
        FROM   dbo.ValidationExpectedSampleRows
        WHERE  RunId = @RunId
    ),
    joined AS (
        SELECT COALESCE(a.SchemaName, e.SchemaName) AS SchemaName,
               COALESCE(a.TableName,  e.TableName)  AS TableName,
               COALESCE(a.Position,   e.Position)   AS Position,
               COALESCE(a.Ordinal,    e.Ordinal)    AS Ordinal,
               a.KeyHash AS ActualKeyHash,
               e.KeyHash AS ExpectedKeyHash,
               a.RowJson AS ActualJson,
               e.RowJson AS ExpectedJson
        FROM   actual_cur   AS a
        FULL OUTER JOIN expected_cur AS e
               ON  a.SchemaName = e.SchemaName
              AND  a.TableName  = e.TableName
              AND  a.Position   = e.Position
              AND  a.Ordinal    = e.Ordinal
    )
    INSERT dbo.ValidationResults
        (RunId, Category, SchemaName, TableName, ObjectName, CheckName,
         ExpectedValue, ActualValue, Status, Details)
    SELECT  @RunId, N'Sample',
            j.SchemaName, j.TableName,
            j.Position + N'#' + CAST(j.Ordinal AS NVARCHAR(10)),
            N'SampleRow',
            CASE WHEN j.ExpectedJson IS NULL THEN NULL
                 WHEN LEN(j.ExpectedJson) > 500
                      THEN LEFT(j.ExpectedJson, 500) + N'...'
                 ELSE j.ExpectedJson END,
            CASE WHEN j.ActualJson IS NULL THEN NULL
                 WHEN LEN(j.ActualJson) > 500
                      THEN LEFT(j.ActualJson, 500) + N'...'
                 ELSE j.ActualJson END,
            CASE WHEN j.ActualJson   IS NULL THEN N'FAIL'
                 WHEN j.ExpectedJson IS NULL THEN N'INFO'
                 WHEN j.ActualKeyHash <> j.ExpectedKeyHash THEN N'WARN'
                 WHEN ISNULL(diffs.MismatchCount, 0) = 0 THEN N'PASS'
                 ELSE N'FAIL' END,
            CASE WHEN j.ActualJson   IS NULL THEN N'Row missing in target'
                 WHEN j.ExpectedJson IS NULL THEN N'No baseline supplied for this position'
                 WHEN j.ActualKeyHash <> j.ExpectedKeyHash
                      THEN N'Key shift: expected ' + ISNULL(j.ExpectedKeyHash, N'<null>')
                         + N', actual ' + ISNULL(j.ActualKeyHash, N'<null>')
                         + N' -- values not compared'
                 WHEN ISNULL(diffs.MismatchCount, 0) = 0 THEN N'All properties match'
                 ELSE CAST(diffs.MismatchCount AS NVARCHAR(10))
                    + N' column(s) differ: ' + diffs.MismatchedCols END
    FROM    joined AS j
    OUTER APPLY (
        SELECT COUNT(*) AS MismatchCount,
               STRING_AGG(d.col, N', ') WITHIN GROUP (ORDER BY d.col) AS MismatchedCols
        FROM (
            SELECT COALESCE(act.[key], exp.[key]) AS col
            FROM   OPENJSON(ISNULL(j.ActualJson,   N'{}')) AS act
            FULL OUTER JOIN OPENJSON(ISNULL(j.ExpectedJson, N'{}')) AS exp
                   ON exp.[key] = act.[key]
            WHERE  ISNULL(act.[value], N'~~NULL~~') COLLATE Latin1_General_BIN2
                <> ISNULL(exp.[value], N'~~NULL~~') COLLATE Latin1_General_BIN2
        ) AS d
    ) AS diffs;

    DECLARE @Pass INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'Sample' AND Status = N'PASS');
    DECLARE @Warn INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'Sample' AND Status = N'WARN');
    DECLARE @Fail INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'Sample' AND Status = N'FAIL');
    DECLARE @Info INT = (SELECT COUNT(*) FROM dbo.ValidationResults WHERE RunId = @RunId AND Category = N'Sample' AND Status = N'INFO');
    PRINT N'[Sample] RunId=' + CONVERT(NVARCHAR(36), @RunId)
        + N' Pass=' + CAST(@Pass AS NVARCHAR(20))
        + N' Warn=' + CAST(@Warn AS NVARCHAR(20))
        + N' Fail=' + CAST(@Fail AS NVARCHAR(20))
        + N' Info=' + CAST(@Info AS NVARCHAR(20));
END;
GO
