-- 2. Reusable per-table sample capture procedure.
-------------------------------------------------------------------------------
CREATE OR ALTER PROCEDURE dbo.sp_ValidationSampleCapture
    @RunId        UNIQUEIDENTIFIER,
    @SchemaName   SYSNAME,
    @TableName    SYSNAME,
    @SampleRows   INT,
    @ColumnList   NVARCHAR(MAX),    -- pre-rendered column list (no SELECT)
    @OrderByAsc   NVARCHAR(MAX),    -- ORDER BY clause for First N (no 'ORDER BY')
    @OrderByDesc  NVARCHAR(MAX),    -- ORDER BY clause for Last  N (no 'ORDER BY')
    @KeyExpr      NVARCHAR(MAX)     -- expression evaluated per row for KeyHash
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @FullName NVARCHAR(260) = QUOTENAME(@SchemaName) + N'.' + QUOTENAME(@TableName);

    IF OBJECT_ID(@FullName) IS NULL
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'Sample', @SchemaName, @TableName, N'TableExists', N'FAIL',
                N'Target table ' + @FullName + N' does not exist.');
        RETURN;
    END;

    IF @SampleRows IS NULL OR @SampleRows <= 0
        SET @SampleRows = 10;

    DECLARE @CaptureSql NVARCHAR(MAX);

    -- First N (ASC) ----------------------------------------------------------
    SET @CaptureSql = N'
        ;WITH src AS (
            SELECT TOP (@N) ' + @ColumnList + N',
                   CONVERT(NVARCHAR(64),
                           HASHBYTES(N''SHA2_256'', CONVERT(VARBINARY(MAX), ' + @KeyExpr + N')),
                           2) AS __KeyHash
            FROM   ' + @FullName + N' WITH (NOLOCK)
            ORDER  BY ' + @OrderByAsc + N'
        ),
        numbered AS (
            SELECT ROW_NUMBER() OVER (ORDER BY (SELECT NULL)) AS __Ord,
                   __KeyHash,
                   ' + @ColumnList + N'
            FROM   src
        )
        INSERT INTO dbo.ValidationSampleRows
            (RunId, SchemaName, TableName, Position, Ordinal, KeyHash, RowJson)
        SELECT @RunId, @Schema, @Table, N''First'', n.__Ord, n.__KeyHash,
               (SELECT ' + @ColumnList + N'
                FOR JSON PATH, INCLUDE_NULL_VALUES, WITHOUT_ARRAY_WRAPPER)
        FROM   numbered AS n;';

    BEGIN TRY
        EXEC sys.sp_executesql @CaptureSql,
            N'@N INT, @RunId UNIQUEIDENTIFIER, @Schema SYSNAME, @Table SYSNAME',
            @N = @SampleRows, @RunId = @RunId, @Schema = @SchemaName, @Table = @TableName;
    END TRY
    BEGIN CATCH
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'Sample', @SchemaName, @TableName, N'CaptureFirst', N'FAIL',
                N'Error capturing First-N sample for ' + @FullName + N': ' + ERROR_MESSAGE());
        RETURN;
    END CATCH;

    -- Last N (DESC) ----------------------------------------------------------
    SET @CaptureSql = N'
        ;WITH src AS (
            SELECT TOP (@N) ' + @ColumnList + N',
                   CONVERT(NVARCHAR(64),
                           HASHBYTES(N''SHA2_256'', CONVERT(VARBINARY(MAX), ' + @KeyExpr + N')),
                           2) AS __KeyHash
            FROM   ' + @FullName + N' WITH (NOLOCK)
            ORDER  BY ' + @OrderByDesc + N'
        ),
        numbered AS (
            SELECT ROW_NUMBER() OVER (ORDER BY (SELECT NULL)) AS __Ord,
                   __KeyHash,
                   ' + @ColumnList + N'
            FROM   src
        )
        INSERT INTO dbo.ValidationSampleRows
            (RunId, SchemaName, TableName, Position, Ordinal, KeyHash, RowJson)
        SELECT @RunId, @Schema, @Table, N''Last'', n.__Ord, n.__KeyHash,
               (SELECT ' + @ColumnList + N'
                FOR JSON PATH, INCLUDE_NULL_VALUES, WITHOUT_ARRAY_WRAPPER)
        FROM   numbered AS n;';

    BEGIN TRY
        EXEC sys.sp_executesql @CaptureSql,
            N'@N INT, @RunId UNIQUEIDENTIFIER, @Schema SYSNAME, @Table SYSNAME',
            @N = @SampleRows, @RunId = @RunId, @Schema = @SchemaName, @Table = @TableName;
    END TRY
    BEGIN CATCH
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'Sample', @SchemaName, @TableName, N'CaptureLast', N'FAIL',
                N'Error capturing Last-N sample for ' + @FullName + N': ' + ERROR_MESSAGE());
        RETURN;
    END CATCH;

    -- Surface empty tables explicitly so the report shows the gap. ----------
    IF NOT EXISTS (
        SELECT 1 FROM dbo.ValidationSampleRows
        WHERE RunId = @RunId AND SchemaName = @SchemaName AND TableName = @TableName
    )
    BEGIN
        INSERT dbo.ValidationResults (RunId, Category, SchemaName, TableName, CheckName, Status, Details)
        VALUES (@RunId, N'Sample', @SchemaName, @TableName, N'SampleCapture', N'INFO',
                N'Table ' + @FullName + N' is empty; no sample rows captured.');
    END;
END;
GO

