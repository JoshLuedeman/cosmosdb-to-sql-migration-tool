/*
================================================================================
 03-SampleDataComparison.sql
 Generated by Cosmos-to-SQL Migration Assessment Tool

 Purpose
   Capture the first-N and last-N rows of each migrated table (ordered by a
   deterministic key) as JSON snapshots, then compare each snapshot row to an
   operator-supplied baseline from the source Cosmos dataset.

 Snapshot table
   dbo.ValidationSampleRows
     - RunId      : groups rows belonging to one validation run.
     - Position   : 'First' | 'Last'.
     - Ordinal    : 1..N (1 = top row of the ASC/DESC sort).
     - KeyHash    : SHA2-256 of the ORDER BY key tuple (PK shift detector).
     - RowJson    : FOR JSON PATH, INCLUDE_NULL_VALUES, WITHOUT_ARRAY_WRAPPER.

 Baseline table (populated externally by the orchestrator)
   dbo.ValidationExpectedSampleRows
     - Same shape, scoped by RunId.
     - The orchestrator MUST re-serialize Cosmos documents with the same
       column projection AND in the same column order used by this script.
       Missing properties are serialized as JSON null (matching
       INCLUDE_NULL_VALUES). Without that contract, the per-property diff
       below will report false positives for property-order or
       null-vs-missing discrepancies.

 Comparison semantics (per (Schema, Table, Position, Ordinal))
   - actual NULL, expected NULL : row not emitted (nothing to compare).
   - actual present, expected NULL : INFO  (no baseline supplied).
   - actual NULL, expected present : FAIL  (row missing in target).
   - both present, KeyHash differs : WARN  (row shift; values not compared).
   - both present, all OPENJSON keys/values equal : PASS.
   - otherwise : FAIL, with the differing column names listed in Details.

 Child tables
   Skipped here (no unique sort key in the assessment model). An INFO row
   per child explains the skip so the report shows the gap.

 Sampling tuning
   :setvar SampleRows 10     (no upper bound; keep small for diff-by-eye)

 Required permissions
   - SELECT on every target schema/table.
================================================================================
*/

